package com.findyourhair.hairanalysis.vo;

public class FaceVo {
	String face;
	String f_exp;
	String style;
	String s_exp;
	
	public String getFace() {
		return face;
	}
	public void setFace(String face) {
		this.face = face;
	}
	public String getF_exp() {
		return f_exp;
	}
	public void setF_exp(String f_exp) {
		this.f_exp = f_exp;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getS_exp() {
		return s_exp;
	}
	public void setS_exp(String s_exp) {
		this.s_exp = s_exp;
	}

}
